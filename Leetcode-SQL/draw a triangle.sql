CREATE PROCEDURE P(R INT)
BEGIN
DECLARE COUNTER INT DEFAULT 1;
WHILE COUNTER <= 20 DO
SELECT REPEAT('* ',COUNTER);
SET COUNTER = COUNTER+1;
END WHILE;
END$$
DELIMITER ;

CALL P(20);

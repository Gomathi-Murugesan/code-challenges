DELIMITER $$
CREATE PROCEDURE P(R int) 
BEGIN
   WHILE R > 0 DO
       SELECT REPEAT("* ", R);
       set R = R - 1;
   END WHILE;
END$$
DELIMITER ;

CALL P(20);	





